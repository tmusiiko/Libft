#include "libft.h"

size_t	ft_strlen(const char *str)

{
	int	i;

	i = 1;
	if (str[i - 1] == '\0')
	{
		return (0);
	}
	while (str[i - 1] != '\0')
	{
		i++;
	}
	return (i - 1);
}
